package fouad.personal.dam.practica;

public class Ej11BucleFOR100Num {

	public static void main(String[] args) {

		/*ESTRUCTURA BUCLE FOR 
		 * (inicializar variable; establecer condición; incremento/decremento) 
		 * byte num=1; --> inicializar variable y darle valor
		 * if (num<=100) --> crear condición
		 * num++; --> establecer incremento/decremento
		 * System.out.println(num);
		 */
		System.out.println("LOS 100 PRIMEROS NÚMEROS");
		for (byte i = 1; i <= 100; i++) {

			System.out.println(i);
		}

	}
}
